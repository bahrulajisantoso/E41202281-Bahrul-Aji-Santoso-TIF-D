package Latihan;

public class StatementForEach {
    public static void main(String[] args) {

        // membuat array
        int [] angka = {10,20,30,40,50};
        // menggunakan perulangan for each untuk menampilkan angka
        for (int x : angka) {
            System.out.print(x + " ");
        }
    }
}
